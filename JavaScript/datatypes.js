//! ==== Datatypes =====
var sname = "Rishi";
console.log(sname);
console.log(typeof sname); // string

var sid = 840;
console.log(sid);
console.log(typeof sid); // number

var isPlaced = false;
console.log(isPlaced);
console.log(typeof isPlaced); // boolean

var isMarried;
console.log(isMarried);
console.log(typeof isMarried); // undefined

var no_of_kids = null;
console.log(no_of_kids);
console.log(typeof no_of_kids); // Object

var wife;
console.log(wife);
console.log(typeof wife); // undefined

var salary = 1n;
console.log(salary);
console.log(typeof salary);

